#квадрат
a1 = int(input('Введите любую длину квадрата в см:'))
P1 = a1*4
S1 = a1*a1
print('Периметр =',P1,'см')
print('Площадь =',S1,'см')

#прямоугольник
a2 = int(input('Введите любую прямоугольника длину в см:'))
b = int(input('Введите любую ширину прямоугольника в см:'))
P2 = 2*(a2+b)
S2 = a2*b
print('Периметр =',P2,'см')
print('Площадь =',S2,'см')

